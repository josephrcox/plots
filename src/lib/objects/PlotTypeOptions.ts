export function getColor(
  typeIndex: number,
  canBeUpgraded = false,
  modifier: number = 22,
) {
  if (typeIndex < 0) {
    if (typeIndex == -1) {
      if (canBeUpgraded) {
        return typeColors["empty_buildable"];
      }
      return typeColors["empty_unusable"];
    } else if (typeIndex == -2) {
      return typeColors["recreation"];
    } else {
      return typeColors["empty_unusable"];
    }
  }
  const plotOption = options[typeIndex];
  let lighterVersion = lightenColor(typeColors[plotOption.type], modifier);
  return lighterVersion;
}
