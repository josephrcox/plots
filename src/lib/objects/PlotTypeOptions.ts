export const lightenColor = (color: string, amount: number = 20): string => {
  const rgbToHsl = (
    r: number,
    g: number,
    b: number,
  ): [number, number, number] => {
    (r /= 255), (g /= 255), (b /= 255);
    const max = Math.max(r, g, b),
      min = Math.min(r, g, b);
    let h = 0,
      s = 0,
      l = (max + min) / 2;

    if (max !== min) {
      const d = max - min;
      s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
      switch (max) {
        case r:
          h = (g - b) / d + (g < b ? 6 : 0);
          break;
        case g:
          h = (b - r) / d + 2;
          break;
        case b:
          h = (r - g) / d + 4;
          break;
      }
      h /= 6;
    }
    return [h * 360, s, l];
  };

  const lightenHSL = (
    h: number,
    s: number,
    l: number,
    amount: number,
  ): string => {
    const newL = Math.min(1, l + amount / 100); // Increase lightness
    return `hsl(${h}, ${(s * 100).toFixed(1)}%, ${(newL * 100).toFixed(1)}%)`;
  };

  const parseRGBA = (color: string): [number, number, number] | null => {
    const match = color.match(/rgba?\((\d+),\s*(\d+),\s*(\d+)/);
    return match
      ? [parseInt(match[1]), parseInt(match[2]), parseInt(match[3])]
      : null;
  };

  let rgb: [number, number, number] | null = null;
  if (color.startsWith("#")) {
    const hex = color.replace("#", "");
    const bigint = parseInt(hex, 16);
    rgb = [(bigint >> 16) & 255, (bigint >> 8) & 255, bigint & 255];
  } else if (color.startsWith("rgba") || color.startsWith("rgb")) {
    rgb = parseRGBA(color);
  }

  if (rgb) {
    const [h, s, l] = rgbToHsl(rgb[0], rgb[1], rgb[2]);
    return lightenHSL(h, s, l, amount);
  }

  throw new Error("Invalid color format");
};
